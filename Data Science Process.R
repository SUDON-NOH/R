# 문제 정의 : 현실의 구체적인 문제를 명확하게 표현하고 통계적, 수리적 언어로 '번역'하는 작업

# 데이터 정의 : 변수, 지표 등을 정의

# 실험 계획 / 표본화 : 데이터를 직접 수집해야 하는 경우는 보통 두 가지 목적 중 하나다. 첫째는 어떤 처리의 효과를 알아내기 위한 통제 실험, 둘쨰는 모집단을 대표하는 표본을 얻기 위한 표본화다. 소스 데이터가 이미 존재하는 경우에는 불필요

# 데이터 취득 : 다양한 형태의, 다양한 시스템에 저장된 원데이터를 분석시스템으로 가져오는 활동

# 데이터 가공 : 데이터를 분석하기 적당한 표 형태로 가공하는 작업, 데이터 변환

# 탐색적 분석과 데이터 시각화 : 시각화와 간단한 통계량을 통하여 데이터의 패턴을 발견하고 이상치를 점검하는 분석

# 모형화 : 모수 추정, 가설검정등의 활동과 모형 분석, 예측 분석 등을 포괄

# 분석 결과 정리 : 분석 결과를 현실적인 언어로 이해하기 쉽도록 번역해내는 작업